-- GENERACIÓN DE UNIVERSO

DROP TABLE IF EXISTS JAT_MCV_UNIVERSO_MODELADO2;

CREATE TABLE IF NOT EXISTS
	JAT_MCV_UNIVERSO_MODELADO2 AS
SELECT
	T1.LLAVE_UNIVERSAL_20,
	T2.FECHA,
	T2.CLIENTE,
	T2.RFC,
	T2.LINEA,
	T1.LABEL,
	T1.REVOLVENTE
FROM
	JEA_MCV_UNIVERSO_BANDERAS_LABELS T1
LEFT JOIN
	JEA_MCV_UNIVERSO T2
ON
	T1.LLAVE_UNIVERSAL_20 = T2.LLAVE_UNIVERSAL_20 AND
	T1.FECHACARTERA = T2.FECHA
WHERE
	T1.CCI = 0 AND
	T1.REESTRUCTURA = 0 AND
	T1.SOBREGIRO = 0 AND
	T1.INTERCOMPANIA = 0 AND
	T1.CARTERA_ADQUIRIDA = 0
;

ALTER TABLE JAT_MCV_UNIVERSO_MODELADO2 ADD KEY LLAVE_UNIVERSAL_20(LLAVE_UNIVERSAL_20);
ALTER TABLE JAT_MCV_UNIVERSO_MODELADO2 ADD KEY FECHA(FECHA);
ALTER TABLE JAT_MCV_UNIVERSO_MODELADO2 ADD KEY CLIENTE(CLIENTE);
ALTER TABLE JAT_MCV_UNIVERSO_MODELADO2 ADD KEY RFC(RFC);
ALTER TABLE JAT_MCV_UNIVERSO_MODELADO2 ADD KEY LINEA(LINEA);

-- AGREGAMOS FOLIO CALIFICA SOBRE EL CUAL TRAEREMOS INFORMACIÓN
ALTER TABLE JAT_MCV_UNIVERSO_MODELADO2 ADD COLUMN FOLIO_BC VARCHAR(15) AFTER LINEA;
ALTER TABLE JAT_MCV_UNIVERSO_MODELADO2 ADD COLUMN FECHA_BC DATE AFTER FOLIO_BC;
ALTER TABLE JAT_MCV_UNIVERSO_MODELADO2 ADD KEY FOLIO_BC(FOLIO_BC);

UPDATE
	JAT_MCV_UNIVERSO_MODELADO2 T1
LEFT JOIN
	(SELECT A.LLAVE_UNIVERSAL_20, A.FECHA, MAX(CASE WHEN YEAR(B.FECHACONSULTA)*100+MONTH(B.FECHACONSULTA) <= A.FECHA*1 THEN B.FOLIO ELSE NULL END) AS FOLIO_BC FROM JAT_MCV_UNIVERSO_MODELADO2 A LEFT JOIN JEA_MCV_CALIFICA B ON A.RFC = B.RFC GROUP BY 1, 2) T2
ON
	T1.LLAVE_UNIVERSAL_20 = T2.LLAVE_UNIVERSAL_20 AND T1.FECHA = T2.FECHA
SET
	T1.FOLIO_BC = T2.FOLIO_BC
;

UPDATE
	JAT_MCV_UNIVERSO_MODELADO2 T1
LEFT JOIN
	(SELECT DISTINCT RFC, FOLIO, FECHACONSULTA FROM JEA_MCV_CALIFICA) T2
ON
	T1.RFC = T2.RFC AND T1.FOLIO_BC = T2.FOLIO
SET
	FECHA_BC = FECHACONSULTA
;

