				ELSE '98: LIQUIDA'
			END
		ELSE RANGO_DA02
	END AS RANGO_DA02,
	COUNT(*) AS REGISTROS,
	ROUND(SUM(SALDO),2) AS SALDO,
	ROUND(SUM(CARTERA_VENCIDA),2) AS CARTERA_VENCIDA
