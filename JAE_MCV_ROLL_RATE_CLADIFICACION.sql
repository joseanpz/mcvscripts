	T0."LLAVE_UNIVERSAL" = T2."LLAVE_UNIVERSAL" AND T0."FECHA" = T2."FECHA"
JOIN
	cartera."JAE_FUNCTION_MCV_ROLL_RATE_CLASIFICACION_"(3, 201611, 201705) T3
ON 
	T0."LLAVE_UNIVERSAL" = T3."LLAVE_UNIVERSAL" AND T0."FECHA" = T3."FECHA"
JOIN
	cartera."JAE_FUNCTION_MCV_ROLL_RATE_CLASIFICACION_"(4, 201611, 201705) T4
ON 
	T0."LLAVE_UNIVERSAL" = T4."LLAVE_UNIVERSAL" AND T0."FECHA" = T4."FECHA"
JOIN
	cartera."JAE_FUNCTION_MCV_ROLL_RATE_CLASIFICACION_"(5, 201611, 201705) T5
